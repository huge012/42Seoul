int	ft_atoi(char *str)
{
	int i;
	int num;
	int minus;

	i = 0;
	num = 0;
	minus = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + str[i] - '0';
		i++;
	}
	if (minus % 2 == 1)
		num = num * -1;
	return (num);
}
